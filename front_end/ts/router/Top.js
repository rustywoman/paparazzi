class Top{
  constructor(app){
    this.app = app;
    this.name = 'top';
  }
  render(){
    this.app.get(
      '/',
      (req, res) => {
        res.render(
          this.name,
          {
            reportList : require('../../../report/index.json')
          }
        );
      }
    );
  }
}


